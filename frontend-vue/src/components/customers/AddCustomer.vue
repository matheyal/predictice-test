<template>
  <div class="submitform">
    <div v-if="!submitted">
      <div class="form-group">
        <label for="name">
          <font-awesome-icon icon="user"/>
          Name</label>
        <input type="text" class="form-control" id="name" required v-model="customer.name" name="name">
      </div>

      <div class="form-group">
        <label for="age">
          <font-awesome-icon icon="ellipsis-v"/>
          Age</label>
        <input type="number" class="form-control" id="age" required v-model="customer.age" name="age">
      </div>

      <button v-on:click="saveCustomer" class="btn btn-success">
        <font-awesome-icon icon="save"/>

        Submit
      </button>
    </div>

    <div v-else>
      <h4>You submitted successfully!</h4>
      <button class="btn btn-success" v-on:click="newCustomer">
        <font-awesome-icon icon="save"/>
        Add
      </button>
    </div>
  </div>
</template>

<script>
  import http from "../../http-common";

  export default {
    name: "add-customer",
    data() {
      return {
        customer: {
          id: 0,
          name: "",
          age: 0,
          active: false
        },
        submitted: false
      };
    },
    methods: {
      /* eslint-disable no-console */
      saveCustomer() {
        var data = {
          name: this.customer.name,
          age: this.customer.age
        };

        http
          .post("/customers", data)
          .then(response => {
            this.customer.id = response.data.id;
            this.$emit("refreshData");
            this.$router.go(-1);
          })
          .catch(e => {
            console.log(e);
          });

        this.submitted = true;
      },
      newCustomer() {
        this.submitted = false;
        this.customer = {};
      }
      /* eslint-enable no-console */
    }
  };
</script>

<style>

  .submitform {
    max-width: 300px;
    margin: auto;
  }
</style>
